public class JoMan {
	public static void main(String[] args) {
		int inputNumber = 45;
		if (inputNumber % 2 == 0) {
			System.out.println(inputNumber + "is even.");
		} else {
			System.out.println(inputNumber + " is odd.");
		}		
	}
}
